    public void arithmeticOperation() {
        switch (calculation) {
            case 1: 
                ans = num + Double.parseDouble(this.txtResult.getText());
                this.txtResult.setText(Double.toString(ans));
                break;
                
            case 2: 
                ans = num - Double.parseDouble(this.txtResult.getText());
                this.txtResult.setText(Double.toString(ans));
                break;
                
            case 3: 
                ans = num * Double.parseDouble(this.txtResult.getText());
                this.txtResult.setText(Double.toString(ans));
                break;
                
            case 4: 
                ans = num / Double.parseDouble(this.txtResult.getText());
                this.txtResult.setText(Double.toString(ans));
                break;
        }
    }
    
