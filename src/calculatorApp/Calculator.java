    private void btnBackSpaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackSpaceActionPerformed
        int length = this.txtResult.getText().length();
        int number = this.txtResult.getText().length() - 1;
        
        String store;
        
        if (length > 0) {
            StringBuilder back = new StringBuilder(this.txtResult.getText());
            back.deleteCharAt(number); 
            store = back.toString();
            this.txtResult.setText(store);
        }
    }//GEN-LAST:event_btnBackSpaceActionPerformed

